var = '   Estou Aprendendo Python neste exato momento  '

#susbstitui certa palavra da variável por outra
print(var.replace('Python', 'Android'))

#transforma letras minúsculas em maiúsculas
print(var.upper())

#transforma letras maiúsculas em minúsculas
print(var.lower())

#deixa apenas a primeira letra da variável maiúscula
print(var.capitalize())

#transforma todas as primeiras letras das palavras em maiúscula
print(var.title())

#remove espaços inúteis do ínicio e do final da variável
print(var.strip())



